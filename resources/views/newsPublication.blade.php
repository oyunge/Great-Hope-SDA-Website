<h6>Musings upon Paul’s Confinement in Caesarea</h6>
<p>I believe that the confinement of Paul in Caesarea was God’s method in reaching out to the two governors Felix and Festus as well as the King Agrippa with the gospel of salvation. Sadly though, all these three people stifled the conviction of the Holy Spirit and refused to repent.
    Felix is described as a corrupt man who loved the wages of unrighteousness. He took bribes and the Jews actually knew this and took advantage of it. He continued to keep Paul in custody yet he knew very well that he was innocent. He was simply trying to please the Jews. He was acquainted with Christianity. He had a form of godliness by associating with Drusila his wife: a Jewess but he denied the power thereof.
    In the judgment hall, Paul, in Acts 24:27 loses sight of his afflictions. He waxes bold in the power of the Holy Spirit, the promised Advocate. He allows the Spirit to take over. He begins to reason of righteousness, self-control and judgment to come. At this moment, Felix is cut to the heart. He trembles. He is convicted of his sin. He realizes that though he is sitting upon a judgment seat, yet, there is another higher Judge who reads the hearts of people and knows all the secrets of life. The Lord of Lords and the Judge of Judges is present in a Roman court room. He is guilty of base crimes. His conscience is fired up. He cannot rest.</p>
<p>Christ had promised that when he the Spirit of Truth is come He shall convict men in regard to sin, and righteousness and judgment. Truly, this judgement seen was a demonstration of this work of the Holy Spirit that Christ had promised in the book of John 16:8.
    But how does Felix treat the Holy Spirit? He tells Paul to go his away and that he will call him at a convenient time. And just like that, Felix shuts the door. He refuses Christ entry into his house to sup with him. He is not excited like Zachaeus to renounce his sin. He is looking for a convenient time.
    But who knows about tomorrow? The only time we have to decide for Christ is today. Christ says that today if you hear this voice do not harden your heart.
    Brothers, Felix story can sound so remote. But, I want to assure you that there is still some Felix, waiting for a convenient time to make a decision for Christ. I know this, because I have also struggled with some habits. And I know that there is something that you are struggling with. It may not be bribery or corruption. But it is a sure sin that will destroy at the end. Won’t this be the most convenient time to heed the call of the Spirit? He promises in Jude 24 that he is able to keep anyone who accepts him from falling. Won’t you accept this promise that you may be partaker of the divine nature?</p>
